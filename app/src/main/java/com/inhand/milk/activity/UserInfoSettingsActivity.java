    @Override
    protected void onCreate(Bundle savedInstanceState) {
        // TODO Auto-generated method stub
        super.onCreate(savedInstanceState);
        output_X = output_Y = getResources().getDimensionPixelOffset(R.dimen.common_settings_item_image_height_width);
        initData();
    }
